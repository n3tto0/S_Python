# Aula de Algoritmo e Lógica
# -->                           Conceito de Protótipo
# O protótipo é uma versão simulada ou amostra de um produto final, a utilizada para testes
# antes do lançamento.
#
# Role prototypes (função)
#       São prot[otipos que possuem o objetivo de invertigar o que um produto pode oferecer# ao usuário. Descreem como a funcionalidade da qual o usuário pode se beneficiar, sem dar # muita atenção ao como funcionaria.
#
# Look & Feel (olhar e sentir)
#       São protótipos com objetico de validar a experiência do usuário com a interface do
# produtom refinando o direcionamento das interações.
#
# Implementation (Implementação)
#       Protótipos de implementação buscam responder dúvidas sobre a viabilidade de imple-
# mentação. Conseguimos fazer isso? Como essa tecnologia pode impocatar na nossa performan-
# ce? Essas são algumas das análises propostas desse tipo de prototipação.
#
# Integration (integração)
#       A categoria final é a união das três anteriores: Role, Look & Feel e Implementation
# 
#       A natureza desses protótipos simula situações reias de uso alta fidelidade, o que
# faz com que sejam mais caros e demorados de construir.
#
# -->                       Protótipos em baixa fidelidade
#
#       Desenhos deitos a mão (Sketches) são a forma mais rápida e barata de tangibilizar  # as ideias. Esse tipo de prototipação pode ser utilizado em diversos momentos e contextos,
# principalmente se o produto ainda não existe, não há toolkit ou componentes digitais que
# podem ser aproveitados para testar a ideia.
#       
#       O protótipo em baixa fidelidade também permite testar:
#               . fluxos menore;
#               . arquitetura;
#               . nomenclatura (Oi, UX Wrinting!);
#               . descrições;
#
#       Se o objetivo é validar a experiência, o protótipo em baixa não é o mais indicado,
# uma vez que ele ainda é pouco fiel ao produto final.
#
#       O protótipo em baixa e uma base opção de aplicação quanto tem o objetivo de:
#               . testar fluxos simpĺes;
#               . coletar feedbacks rápidos;
#               . validar texto (test A/B)
#               . organizar as ideias.
#
#       Ele não é uma boa opção para:
#               . validar a experiência final;
#               . avaliar a usabilidade;
#               . validar tarefas complexas.
#
#
# -->                       Protótipos em média fidelidade
#
#       O que é protótipação em média fidelidade? Ela resulta em portótipos um pouco mais t# rabalhosos e que se aproximam melhor da interface final. Para ser contruído começam a de-# pender de ferramentas de protótipação mais complexas. como Figma, Sketch e InVision.
#
#       Wireframes, apesar de estarem caindo em desuso no cenário de desenvlvimento ágil de# de produto, entram nessa categoria. Já foram importantes - principalmente para a arquite-
# tura de informação - quando sites começaram a se popularizar e o uso de interface digi-
# tais ainda não fazia parte do modelo mental das pessoas. Ainda são muito comuns no desen-# volvimento de sites como uma etapa de validaão do cliente.
#
#       Os protótipod em média fidelidade são boas opções para:
#           ·        organizar as ideias;
#           ·        testar fluxos e tarefas;
#           ·        coletar feedbacks em tempo moderado;
#           ·        validar textos (teste A/B) e estrutura da interface.
#
#
#       Entretanto, não são indicados para:
#           . validar a experiência;
#           . usabilidade finais.

